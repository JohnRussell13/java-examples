package com.goldrush;

public class PosCrd {
    private float x;
    private float y;
    
    public PosCrd() {
        x = 0;
        y = 0;
    }

    public float getX(){
        return x;
    }

    public float getY(){
        return y;
    }

    public void setX(float val){
        x = val;
    }

    public void setY(float val){
        y = val;
    }
}
